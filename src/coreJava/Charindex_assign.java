package coreJava;

public class Charindex_assign {
	public static void main(String[] args) {
		
        String str = "Tops Technologies!";
        char char1 = str.charAt(0);
        char char2 = str.charAt(10);
        System.out.println("Original String = " + str);
        System.out.println("The character at position  0 is : " + char1);
        System.out.println("The character at position  10 is : " + char2);
    }
}
